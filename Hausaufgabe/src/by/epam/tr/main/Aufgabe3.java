public class Aufgabe3 {

	public static void main(String[] args) {
		float x;
		float y;
		
		float sum;
		float unt;
		float pro;
		float quo;
				
		x = 14;
		y = 11;
		
		sum = x + y;
		unt = x - y;
		pro = x * y;
		quo = x / y;		
		
		System.out.println("Summe="+sum);
		System.out.println("Unterschied="+unt);
		System.out.println("Produkt="+pro);
		System.out.println("Quotient="+quo);
		

	}

}
